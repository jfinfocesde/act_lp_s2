package com.example;

public class Ejercicio2 {
    public static void ejercicio2() {
        System.out.println("Ejercicio 2");


        byte byteVariable = 100;               // byte
        short shortVariable = 1000;             // short
        int intVariable = 50000;               // int
        long longVariable = 1000000L;          // long
        float floatVariable = 3.14f;           // float
        double doubleVariable = 2.71828;       // double
        char charVariable = 'A';               // char
        boolean booleanVariable = false;       // boolean

       
        System.out.println("Tipo: byte, Valor: " + byteVariable);
        System.out.println("Tipo: short, Valor: " + shortVariable);
        System.out.println("Tipo: int, Valor: " + intVariable);
        System.out.println("Tipo: long, Valor: " + longVariable);
        System.out.println("Tipo: float, Valor: " + floatVariable);
        System.out.println("Tipo: double, Valor: " + doubleVariable);
        System.out.println("Tipo: char, Valor: " + charVariable);
        System.out.println("Tipo: boolean, Valor: " + booleanVariable);
  
    }
}
 
